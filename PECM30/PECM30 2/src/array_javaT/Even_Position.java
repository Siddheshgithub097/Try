package array_javaT;

public class Even_Position 
{
	public static void main(String[] args) 
	{																// print only even position array element .
				int array [] = {34,65,87,98,56,78,56,45,87,34};
				
				for (int i = 1; i < array.length; i=i+2) 
				{
					
						System.out.println(array[i]+" ");
					
				}
			}
		

	
}
