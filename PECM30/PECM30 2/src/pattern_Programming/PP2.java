package pattern_Programming;

public class PP2 
{
	public static void main(String[] args) 
	{
		PP2(5);
	}
	
	public static void PP2(int n )
	{
		for (int col = 1; col <= n; col++) 
		{
			for (int i = 1; i <= n; i++) 
			{
				
				System.out.print("* ");
			}
			
			System.out.println();
		}
	}
}
